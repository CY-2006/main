    private void pintartabla(){
        String encabezado[]={"Codigo","Descuento","Vencimiento","Tipo de descuento"};
        DefaultTableModel t = new DefaultTableModel(encabezado,Proyecto_10.cupones.size());
        jTable1.setModel(t);
        TableModel tabla = jTable1.getModel();
        
        for(int i =0;i<Proyecto_10.cupones.size();i++){
         Cupones u = Proyecto_10.cupones.get(i);
         tabla.setValueAt(u.getCodigo(), i, 0);
         tabla.setValueAt(u.getDescuento(), i, 1);
         tabla.setValueAt(u.getFecha_limite(), i, 2);
         tabla.setValueAt(u.getMonto(), i, 3);
        }
                
    }
