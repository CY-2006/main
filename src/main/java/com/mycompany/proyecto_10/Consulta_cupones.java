private  String arreglo[]=null;
    public void  cargarArchivo(File archivo){
    
    FileReader fr = null;
    BufferedReader br = null;
    
    try{
        
        fr =new FileReader(archivo);
        br = new BufferedReader(fr);
       System.out.println(" Codigo " +" Descuento "+" Vencimiento "+" Tipo de descuento ");
 
        String linea ;
        while((linea = br.readLine()) !=null ){
           arreglo = linea.split("\\|");
              
        if(arreglo.length==4){
            
        Cupones u = new Cupones();
          u.setCodigo(arreglo[0]);
          u.setDescuento(Double.parseDouble(arreglo[1]));
          u.setFecha_limite(arreglo[2]);
          u.setMonto(arreglo[3]);
          
          Proyecto_10.cupones.add(u);

        }      
    }
        pintartabla();
        fr.close();
    }catch(Exception ex){
ex.printStackTrace();
        }
}
 
    
